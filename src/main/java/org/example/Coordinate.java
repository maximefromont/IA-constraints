package org.example;

public class Coordinate {

    //PRIVATE ATTRIBUTES
    private int x;
    private int y;

    //CONSTRUCTOR
    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //PUBLIC INTERFACE
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
